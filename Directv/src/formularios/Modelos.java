        public boolean cargarDialogo()
        {
         int fila = jtb_Modelos.getSelectedRow();  
        
        if( fila == -1 ) 
        {
            JOptionPane.showMessageDialog(null,"Debe seleccionar modelos","Atencion",JOptionPane.WARNING_MESSAGE ); 
            return false;
        }
        else
        {
            DefaultTableModel modelo = new DefaultTableModel();
            
            modelo = (DefaultTableModel) jtb_Modelos.getModel();
            
            txt_CodigoModelo.setText( modelo.getValueAt(fila,0).toString());
            txt_ModeloIrd.setText( modelo.getValueAt(fila,1).toString());
            
            
            
            
            return true;
         }  
    
     }
