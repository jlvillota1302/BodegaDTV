    private void btn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AceptarActionPerformed
        
        String estado;
        Connection con = conectarBD();
        
        if(accion.equals("ADICIONAR"))
        {
            try
             
            {
                String sSQL="INSERT INTO tipos_de_servicio VALUES (?,?)";
                PreparedStatement pst = con.prepareStatement(sSQL);

                

                pst.setString(1, txt_CodigoServicio.getText());
                pst.setString(2, txt_NombreServicio.getText());
               
                
                JOptionPane.showMessageDialog(null, " "+txt_CodigoServicio.getText()+" "+txt_NombreServicio.getText());

      
                pst.execute();
            }       
      
            catch (Exception e)
              {
               
                  
              
              }
        }    
        else if (accion.equals("MODIFICAR"))
        {
          
            try {
                String sSQL="update tipos_de_servicio set codigoservicio=?, where nombreservicio=?";
                PreparedStatement pst = con.prepareStatement(sSQL);
                
                pst.setString(1, txt_CodigoServicio.getText());
                pst.setString(2, txt_NombreServicio.getText());
                pst.execute();
            } catch (SQLException ex) {
                Logger.getLogger(TiposServicio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        else
        {
            try
            {
                String sSQL = "DELETE FROM tipos_de_servicio WHERE codigoservicio=?";
                
                PreparedStatement pst = con.prepareStatement(sSQL);
                
                pst.setString(1, txt_CodigoServicio.getText());
                pst.execute();
                
            }
            catch ( Exception e )
            {
                JOptionPane.showMessageDialog(null, "Error al eliminar el Tipo de Servicio","Atencion",JOptionPane.WARNING_MESSAGE);
            }
        }
        
        cargarTabla("");
        desconectarBD();
        jdi_TipoServicio.dispose();
    }//GEN-LAST:event_btn_AceptarActionPerformed

    private void btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarActionPerformed
                
        String sSQL = "";
        String buscar = txt_Buscar.getText();
        int campo = cmb_Buscar.getSelectedIndex();
        
        if( campo == 0 )  // Todos
        {
           sSQL = "";
        }
        else if( campo == 1 ) // documento
        {
           sSQL =  " WHERE codigoservicio LIKE '%"+buscar+"%'";
        }
        else
        {
           sSQL =  " WHERE nombreservicio LIKE '%"+buscar+"%'";
        }
   
   
        cargarTabla(sSQL);
    }//GEN-LAST:event_btn_BuscarActionPerformed
    
    
