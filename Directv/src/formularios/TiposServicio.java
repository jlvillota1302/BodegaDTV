    public void cargarTabla(String condicion)
        {
            Connection con = conectarBD();
            ResultSet   rs = null;
            String[]   titulos = {"codigoservicio","nombreservicio",};
            Object[]  fila = new Object[2]; 
            DefaultTableModel modelo = new DefaultTableModel(null,titulos);
            
            String sSQL  = "SELECT * FROM tipos_de_servicio"+condicion;
            
            try {
                
                PreparedStatement pst = con.prepareStatement(sSQL);
                rs=pst.executeQuery();
                
                while(rs.next ())
                {
                    fila[0] = rs.getString("codigoservicio");
                    fila[1] = rs.getString("nombreservicio");
                    
                    modelo.addRow(fila);
                }  
                
                jtb_TipoServicio.setModel(modelo);
            } 
            catch (Exception e) 
            {
            }
            desconectarBD();
        }
        
