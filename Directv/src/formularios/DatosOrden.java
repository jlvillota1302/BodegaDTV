    private void btn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AceptarActionPerformed
        
        String estado;
        Connection con = conectarBD();
        
        if(accion.equals("ADICIONAR"))
        {
            try
             
            {
                String sSQL="INSERT INTO Datos de orden VALUES (?,?,?,?,?,?,?)";
                PreparedStatement pst = con.prepareStatement(sSQL);

                

                pst.setString(1, txt_Work.getText());
                pst.setString(2, txt_Shiping.getText());
                pst.setString(3, txt_Ibs.getText());
                pst.setString(4, txt_Codigo.getText());
                pst.setString(5, txt_Serial.getText());
                pst.setString(6, txt_Documento.getText());
                pst.setString(7, txt_NumOrden.getText());
                
                JOptionPane.showMessageDialog(null, " "+txt_Work.getText()+" "+txt_Shiping.getText()+" "+txt_Ibs.getText()+" " +txt_Codigo.getText()+" "+txt_Serial.getText()+" "+txt_Documento+" "+txt_NumOrden);

      
                pst.execute();
            }       
      
            catch (Exception e)
              {
               
                  
              
              }
        }    
        else if (accion.equals("MODIFICAR"))
        {
          
            try {
                String sSQL="update Datos de Orden set workorder=?,Shipingorder=?,ibs=?,codigo=?,serial=?,documento=? where numero_orden=?";
                PreparedStatement pst = con.prepareStatement(sSQL);
                
                pst.setString(1, txt_Work.getText());
                pst.setString(2, txt_Shiping.getText());
                pst.setString(3, txt_Ibs.getText());
                pst.setString(4, txt_Codigo.getText());
                pst.setString(5, txt_Serial.getText());
                pst.setString(6, txt_Documento.getText());
                pst.setString(7, txt_NumOrden.getText());
                pst.execute();
            } catch (SQLException ex) {
                Logger.getLogger(DatosOrden.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        else
        {
            try
            {
                String sSQL = "DELETE FROM datos_orden_de_servicio WHERE cli_documento=?";
                
                PreparedStatement pst = con.prepareStatement(sSQL);
                
                pst.setString(1, txt_Work.getText());
                pst.execute();
                
            }
            catch ( Exception e )
            {
                JOptionPane.showMessageDialog(null, "Error al eliminar el Dato de orden","Atencion",JOptionPane.WARNING_MESSAGE);
            }
        }
        
        cargarTabla("");
        desconectarBD();
        jdi_DatosOrden.dispose();
    }//GEN-LAST:event_btn_AceptarActionPerformed

    
    
