     public void cargarTabla(String condicion)
        {
            Connection con = conectarBD();
            ResultSet   rs = null;
            String[]   titulos = {"workorder","shipingorder","ibs","codigoservicio","serial","documento","numero_orden"};
            Object[]  fila = new Object[7]; 
            DefaultTableModel modelo = new DefaultTableModel(null,titulos);
            
            String sSQL  = "SELECT * FROM Datos de Orden"+condicion;
            
            try {
                
                PreparedStatement pst = con.prepareStatement(sSQL);
                rs=pst.executeQuery();
                
                while(rs.next ())
                {
                    fila[0] = rs.getString("workorder");
                    fila[1] = rs.getString("shipingorder");
                    fila[2] = rs.getString("ibs");
                    fila[3] = rs.getString("codigoservicio");
                    fila[4] = rs.getString("serial");
                    fila[5] = rs.getString("documento");
                    fila[6] = rs.getString("numero_orden");
                    modelo.addRow(fila);
                }  
                
                jtb_Datos.setModel(modelo);
            } 
            catch (Exception e) 
            {
            }
            desconectarBD();
        }
        
